#!/usr/bin/env node
const fs = require('fs');
const path = require('path');
const { Client } = require('pg');

const connectionString = process.env.NEON_DATABASE_URL || process.env.DATABASE_URL;
const outputPath = path.resolve(process.cwd(), 'docs', 'database-schema.generated.md');

const SQL = {
  tables: `
    SELECT
      table_schema,
      table_name
    FROM information_schema.tables
    WHERE table_type = 'BASE TABLE'
      AND table_schema NOT IN ('pg_catalog', 'information_schema')
    ORDER BY table_schema, table_name;
  `,
  columns: `
    SELECT
      table_schema,
      table_name,
      column_name,
      data_type,
      is_nullable,
      column_default
    FROM information_schema.columns
    WHERE table_schema NOT IN ('pg_catalog', 'information_schema')
    ORDER BY table_schema, table_name, ordinal_position;
  `,
  primaryKeys: `
    SELECT
      tc.table_schema,
      tc.table_name,
      tc.constraint_name,
      kcu.column_name,
      kcu.ordinal_position
    FROM information_schema.table_constraints AS tc
    JOIN information_schema.key_column_usage AS kcu
      ON tc.constraint_name = kcu.constraint_name
     AND tc.table_schema = kcu.table_schema
    WHERE tc.constraint_type = 'PRIMARY KEY'
      AND tc.table_schema NOT IN ('pg_catalog', 'information_schema')
    ORDER BY tc.table_schema, tc.table_name, tc.constraint_name, kcu.ordinal_position;
  `,
  foreignKeys: `
    SELECT
      tc.table_schema,
      tc.table_name,
      tc.constraint_name,
      kcu.column_name,
      ccu.table_schema AS foreign_table_schema,
      ccu.table_name AS foreign_table_name,
      ccu.column_name AS foreign_column_name,
      rc.update_rule,
      rc.delete_rule
    FROM information_schema.table_constraints AS tc
    JOIN information_schema.key_column_usage AS kcu
      ON tc.constraint_name = kcu.constraint_name
     AND tc.table_schema = kcu.table_schema
    JOIN information_schema.constraint_column_usage AS ccu
      ON ccu.constraint_name = tc.constraint_name
     AND ccu.constraint_schema = tc.table_schema
    JOIN information_schema.referential_constraints AS rc
      ON rc.constraint_name = tc.constraint_name
     AND rc.constraint_schema = tc.table_schema
    WHERE tc.constraint_type = 'FOREIGN KEY'
      AND tc.table_schema NOT IN ('pg_catalog', 'information_schema')
    ORDER BY tc.table_schema, tc.table_name, tc.constraint_name, kcu.ordinal_position;
  `,
  indexes: `
    SELECT
      schemaname AS table_schema,
      tablename AS table_name,
      indexname AS index_name,
      indexdef AS index_definition
    FROM pg_indexes
    WHERE schemaname NOT IN ('pg_catalog', 'information_schema')
    ORDER BY schemaname, tablename, indexname;
  `,
  views: `
    SELECT
      table_schema,
      table_name AS view_name,
      view_definition
    FROM information_schema.views
    WHERE table_schema NOT IN ('pg_catalog', 'information_schema')
    ORDER BY table_schema, table_name;
  `,
};

function fail(message) {
  console.error(message);
  process.exitCode = 1;
}

function quoteMarkdown(value) {
  if (value === null || value === undefined || value === '') return '';
  return String(value).replace(/\|/g, '\\|').replace(/\r?\n/g, '<br>');
}

function markdownTable(headers, rows) {
  if (!rows.length) return '_None found._';

  const headerRow = `| ${headers.join(' | ')} |`;
  const separatorRow = `| ${headers.map(() => '---').join(' | ')} |`;
  const bodyRows = rows.map(row => `| ${row.map(quoteMarkdown).join(' | ')} |`);
  return [headerRow, separatorRow, ...bodyRows].join('\n');
}

function qualifiedName(schema, name) {
  return `${schema}.${name}`;
}

function groupPrimaryKeys(rows) {
  const grouped = new Map();
  for (const row of rows) {
    const key = `${row.table_schema}.${row.table_name}.${row.constraint_name}`;
    if (!grouped.has(key)) {
      grouped.set(key, {
        table_schema: row.table_schema,
        table_name: row.table_name,
        constraint_name: row.constraint_name,
        columns: [],
      });
    }
    grouped.get(key).columns.push(row.column_name);
  }
  return [...grouped.values()];
}

function buildMarkdown(audit) {
  const generatedAt = new Date().toISOString();
  const primaryKeys = groupPrimaryKeys(audit.primaryKeys);

  return [
    '# Database Schema Audit',
    '',
    `Generated: \`${generatedAt}\``,
    '',
    '> Read-only schema snapshot generated by `scripts/audit-db-schema.js`.',
    '',
    '## Tables',
    '',
    markdownTable(
      ['table_name'],
      audit.tables.map(row => [qualifiedName(row.table_schema, row.table_name)]),
    ),
    '',
    '## Columns',
    '',
    markdownTable(
      ['table_name', 'column_name', 'data_type', 'is_nullable', 'column_default'],
      audit.columns.map(row => [
        qualifiedName(row.table_schema, row.table_name),
        row.column_name,
        row.data_type,
        row.is_nullable,
        row.column_default,
      ]),
    ),
    '',
    '## Primary Keys',
    '',
    markdownTable(
      ['table_name', 'constraint_name', 'columns'],
      primaryKeys.map(row => [
        qualifiedName(row.table_schema, row.table_name),
        row.constraint_name,
        row.columns.join(', '),
      ]),
    ),
    '',
    '## Foreign Keys',
    '',
    markdownTable(
      ['table_name', 'constraint_name', 'column_name', 'references', 'update_rule', 'delete_rule'],
      audit.foreignKeys.map(row => [
        qualifiedName(row.table_schema, row.table_name),
        row.constraint_name,
        row.column_name,
        `${qualifiedName(row.foreign_table_schema, row.foreign_table_name)}.${row.foreign_column_name}`,
        row.update_rule,
        row.delete_rule,
      ]),
    ),
    '',
    '## Indexes',
    '',
    markdownTable(
      ['table_name', 'index_name', 'index_definition'],
      audit.indexes.map(row => [
        qualifiedName(row.table_schema, row.table_name),
        row.index_name,
        row.index_definition,
      ]),
    ),
    '',
    '## Views',
    '',
    markdownTable(
      ['view_name', 'view_definition'],
      audit.views.map(row => [
        qualifiedName(row.table_schema, row.view_name),
        row.view_definition,
      ]),
    ),
    '',
  ].join('\n');
}

function printSummary(audit) {
  const primaryKeys = groupPrimaryKeys(audit.primaryKeys);

  console.log('Database schema audit');
  console.log('---------------------');
  console.log(`Tables: ${audit.tables.length}`);
  audit.tables.forEach(row => console.log(`  - ${qualifiedName(row.table_schema, row.table_name)}`));
  console.log(`Columns: ${audit.columns.length}`);
  console.log(`Primary keys: ${primaryKeys.length}`);
  console.log(`Foreign keys: ${audit.foreignKeys.length}`);
  console.log(`Indexes: ${audit.indexes.length}`);
  console.log(`Views: ${audit.views.length}`);
  console.log(`Markdown written: ${outputPath}`);
}

async function main() {
  if (!connectionString) {
    fail('Missing database URL. Set NEON_DATABASE_URL or DATABASE_URL before running this script.');
    return;
  }

  const client = new Client({ connectionString });

  try {
    await client.connect();
    await client.query('BEGIN READ ONLY');

    const audit = {};
    for (const [key, sql] of Object.entries(SQL)) {
      const result = await client.query(sql);
      audit[key] = result.rows;
    }

    await client.query('ROLLBACK');

    const markdown = buildMarkdown(audit);
    fs.mkdirSync(path.dirname(outputPath), { recursive: true });
    fs.writeFileSync(outputPath, markdown, 'utf8');

    printSummary(audit);
  } catch (error) {
    try {
      await client.query('ROLLBACK');
    } catch (_) {
      // Ignore rollback errors when the connection failed before a transaction began.
    }
    fail(`Schema audit failed: ${error.message}`);
  } finally {
    await client.end().catch(() => {});
  }
}

main();
